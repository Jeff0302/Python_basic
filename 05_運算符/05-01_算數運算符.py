# 算術運算符
# + 加法運算符 (如果是兩個字符串之間進行加法運算，則會進行拼串操作)
# - 減法運算符
# * 乘法運算符 (如果是將字符串和數字相乘，則會對字符串進行複製操作，將字符串重複指定次數)
# / 除法運算符, 運算結果會返回一個浮點類型
# //整除, 只會保留計算後的整數位, 返回整型
# **冪運算, 求一個值的幾次冪
# % 取模(餘)

a = 10 + 5
a = 'hello' + ' ' + 'world'

a = 10 - 5 # 5
a = 10 - True # 1
a = a - 2 # 8

a = 5 * 5 # 25
a = 10 / 5 # 2.0
a = 10 / 3 # 3.333...
a = 10 // 3 # 3

a = 2 ** 2 # 4
a = 16 ** 0.5   # 求16的平方根, 4

a = 10 % 2     # 0
a = 10 % 4     # 2
print("a= ", a)
