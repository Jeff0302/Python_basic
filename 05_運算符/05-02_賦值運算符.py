# 賦值運算符
"""
    = 可以將等號右側的值賦值給等號左側的變量
    += : a += 5 相當於 a = a + 5
    -= : a -= 5 相當於 a = a - 5
    *= : a *= 5 相當於 a = a * 5
    **= : a **= 5 相當於 a = a ** 5
    /= : a /= 5 相當於 a = a / 5
    //= : a // = 5 相當於 a = a // 5
    %= : a % = 5 相當於 a = a % 5

"""

a = 10
a += 5
print('a= ',a) # 15
a -= 5
print('a= ',a) # 10
a **= 2
print('a= ',a) # 100
a /= 25
print('a= ',a) # 4.0
a = 25
a //= 5
print('a= ',a) # 5.0
a = 5
a %= 4
print('a= ',a) # 1
