# 字面量和變量
"""
    字面量就是一個一個值，比如:1, 2, 3, 4, 5, 6, 'Hello'
        字面量所表示的意思就是它字面的值，在程序中可以直接使用字面量

    變量(variable)變量可以用來保存字面量，並且變量中保存的字面量是不定的
        變量本身沒有任何意思，它會根據不同字面量表示不同意思
        一般我們在開發時，很少直接使用字面量，都是將字面量保存到變量中，
        通過變量來引用字面量


"""

if __name__ == '__main__':
    print('Hello world')
    a = 'Hello world'
    print(a)
